#ifndef TRAVIS_CPP_CALCULADORA_H
#define TRAVIS_CPP_CALCULADORA_H

int sumar(int n1, int n2){

    return n1 + n2;

}

int restar(int n1, int n2){

    return n1 - n2;

}
int multiplicar(int n1, int n2){

    return n1 * n2;

}

int dividir(int n1, int n2){

    return n1 / n2;

}

#endif //TRAVIS_CPP_CALCULADORA_H
